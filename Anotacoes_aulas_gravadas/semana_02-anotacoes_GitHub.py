# DICIONÁRIO GIT E GITHUB:

# -Overview -> visão geral
# -Default -> padrão
# -Open Source -> código aberto
# -Debian e Ubuntu -> Sistemas operacionais Linux disponíveis livremente.
# -Mobile -> tecnologia portátil, que permite movimentar com ela.
# -Terminal-> tela preta de comandos sem Interface Grafica
# -Versionamento -> processo de controle de versões de código
# -Repositório -> pasta onde armazenamos o código em subpastas ou arquivos.
# -Issues -> no github, é onde ocorrem as contribuições ou reportam os problemas/bugs. (issues=problemas)
# -Commit -> É uma alteração individual de arquivo. O git cria ID (SHA ou hash)
# -Diff -> diferença de alterações entre dois commits
# -Alterações -> qualquer modificação
# -Branch -> versão paralela à principal que pode ser "mergeada" posteriormente.
# -Branch de recurso (teste/homologação)-> usada para testes de recursos ou corrigir problemas que não estejam em produção (branch de tópico). 
# -Branch de produção (ou master) -> pronta para produção, uso, publicação. 
# -Merge -> através da 'Pull Request', solicita-se a Merge de um brancha outro.
# -Pull Request (PR) ->
# -Clone -> cópia de repositório no seu computador.





